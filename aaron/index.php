<h1 align = "center" class="aaron_home"> I] What is Diabetes?</h1>

<p class="aaron_home_body" >Diabetes is the condition in which the body does not properly process food for use as energy. Most of the food we eat is turned into glucose, or sugar, for our bodies to use for energy. The pancreas, an organ that lies near the stomach, makes a hormone called insulin to help glucose get into the cells of our bodies.

    </p>

<br>

<hr class="aaron_hr_style">

<h1 align ="center" class="aaron_home"> II] Most Common Forms Of Diabetes </h1>






<div class="aaron_home2">
1) Type 2 diabetes <br>
A chronic condition that affects the way the body processes blood sugar (glucose).</div>
<br><br>

<div class="aaron_home">
2) Type 1 diabetes <br>
A chronic condition in which the pancreas produces little or no insulin.
 </div>   
<br><br>    
    
<div class="aaron_home2">
3) Prediabetes <br>
A condition in which blood sugar is high, but not high enough to be type 2 diabetes.
</div>
<br><br>

<div class="aaron_home">
4) Gestational diabetes <br>
A form of high blood sugar affecting pregnant women.
</div>


